print('O tipo da variavel é :', type(n))
print('é espaço?',n.isspace())
print('é número?',n.isnumeric())
print('é letra?',n.isalpha())
print('é alfanumerico?',n.isalnum())
print('é maiusculo?',n.isupper())
print('é minusculo?',n.islower())
print('é impresso?',n.isprintable())
print('é capitalizada?',n.istitle())

